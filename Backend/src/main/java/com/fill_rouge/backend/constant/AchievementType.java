package com.fill_rouge.backend.constant;

public enum AchievementType {
    PARTICIPATION,          // Based on number of events attended
    HOURS_CONTRIBUTED,      // Based on total volunteer hours
    CATEGORY_SPECIALIST,    // Based on participation in specific category
    HIGH_RATING,           // Based on average rating received
    STREAK,                // Based on consecutive event participation
    FIRST_TIME,            // First-time achievements (one-time)
    MILESTONE,             // Milestone achievements (progressive)
    SPECIAL               // Special or seasonal achievements
} 